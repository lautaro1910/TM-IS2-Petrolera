    public void cargarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) this.jTable1.getModel();
        modelo = (DefaultTableModel) this.jTable1.getModel();
        // elimina si tenia algo
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();

        Set listaYacimientoBD = yd.getDAO();

        Object[] fila;
        Iterator<Yacimiento> it = listaYacimientoBD.iterator();

        while (it.hasNext()) {
            String nuevoEstado = "";
            fila = new Object[3];
            Yacimiento aiter = it.next();
            if (aiter.getEstadoYacimiento() == 1) {
                nuevoEstado = "Activo";
            } else {
                nuevoEstado = "Inactivo";
            }
            fila[0] = aiter.getLocalizacionOrigen();
            fila[1] = aiter.getNombreYacimiento();
            fila[2] = nuevoEstado;

            modelo.addRow(fila);
        }
    }

